package com.naver.dto;

public class HugiDto {
	
	private int num;
	private int m_name;
	private int name;
	private String title;
	private String content;
	private int grade;
	
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	public int getM_name() {
		return m_name;
	}
	public void setM_name(int m_name) {
		this.m_name = m_name;
	}
	public int getName() {
		return name;
	}
	public void setName(int name) {
		this.name = name;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	public int getGrade() {
		return grade;
	}
	public void setGrade(int grade) {
		this.grade = grade;
	}

	
	
}
